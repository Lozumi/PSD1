#include <stdio.h>

int main(void)
{
    float a, b, c;
    scanf("%f", &a);
    if (1 < a && a <= 5000)
        b = 0 * a;
    else if (5000 < a && a <= 8000) // 3000 90
        b = 0.03 * (a - 5000);
    else if (8000 < a && a <= 17000) // 9000 900
        b = 90 + 0.1 * (a - 8000);
    else if (17000 < a && a <= 30000) // 13000 2600
        b = 990 + 0.2 * (a - 17000);
    else if (30000 < a && a <= 40000) // 10000 2500
        b = 3590 + 0.25 * (a - 30000);
    else if (40000 < a && a <= 60000) // 20000 6000
        b = 6090 + 0.3 * (a - 40000);
    else if (60000 < a && a <= 85000) // 25000 8750
        b = 12090 + 0.35 * (a - 60000);
    else if (85000 < a)
        b = 20840 + 0.45 * (a - 85000);
    c = a - b;
    printf("应发：%.2f, 个人所得税：%.2f，实发：%.2f", a, b, c);
    return 0;
}